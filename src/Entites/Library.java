package Entites;

public class Library {
    private String libraryName;
    private String aboutLibrary;
    private String libraryAdress;

    //getter and setter

    public String getLibraryName() {
        return libraryName;
    }

    public void setLibraryName(String libraryName) {
        this.libraryName = libraryName;
    }

    public String getAboutLibrary() {
        return aboutLibrary;
    }

    public void setAboutLibrary(String aboutLibrary) {
        this.aboutLibrary = aboutLibrary;
    }

    public String getLibraryAdress() {
        return libraryAdress;
    }

    public void setLibraryAdress(String libraryAdress) {
        this.libraryAdress = libraryAdress;
    }
}
